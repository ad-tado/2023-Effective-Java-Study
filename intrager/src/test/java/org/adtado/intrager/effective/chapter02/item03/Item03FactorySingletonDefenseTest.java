package org.adtado.intrager.effective.chapter02.item03;

import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;

import java.lang.reflect.Constructor;
import java.lang.reflect.InvocationTargetException;

import static org.assertj.core.api.Assertions.assertThat;
import static org.junit.jupiter.api.Assertions.*;

class Item03FactorySingletonDefenseTest {

    @DisplayName("리플렉션을 이용해 싱글톤 파괴 시도 시 Exception 발생")
    @Test
    void reflectionsSingletonViolationTest() throws NoSuchMethodException {
        // given
        Constructor<Item03FactorySingletonDefense> constructor = Item03FactorySingletonDefense.class.getDeclaredConstructor();
        constructor.setAccessible(true);

        // when

        // then
        Exception e = assertThrows(InvocationTargetException.class, constructor::newInstance);
        assertThat("class " + e.getCause())
                .isEqualTo(RuntimeException.class + ": You can create only one INSTANCE of static factory method");
    }
}